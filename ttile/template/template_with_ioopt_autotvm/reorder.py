    fuse = []
    fuse_int = []
    for k in range(len(l)):
        if "axe_kernel" not in l[k] and "axe_in_channels" not in l[k]:
            fuse_int +=[l[k]]
        else:
            if len(fuse_int) != 0:
                fuse += [fuse_int]
                fuse_int = []
    fuse += [fuse_int]
    if len(fuse) == 0:
        return fuse_int
    else:
        maxi = 0
        id_max = 0
        for k in range(len(fuse)):
            if len(fuse) > maxi:
                maxi = len(fuse)
                id_max = k
        return fuse[id_max]


